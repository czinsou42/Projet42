#include "pipex.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*d;

	i = 0;
	d = (char *) malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (!(d))
		return (NULL);
	while (s[i])
	{
		d[i] = s[i];
		i++;
	}
	d[i] = '\0';
	return (d);
}

